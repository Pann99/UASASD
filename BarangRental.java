class BarangRental {
    String nomorTNKB;
    String namaKendaraan;
    String jenis;
    int tahun;
    double biayaSewaPerJam;

    BarangRental(String nomorTNKB, String namaKendaraan, String jenis, int tahun, double biayaSewaPerJam) {
        this.nomorTNKB = nomorTNKB;
        this.namaKendaraan = namaKendaraan;
        this.jenis = jenis;
        this.tahun = tahun;
        this.biayaSewaPerJam = biayaSewaPerJam;
    }
}